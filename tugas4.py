a = 9
b = 13

print(" ================== Lebih besar dari 13 ")
hasil = a > 13
print(a, "> 13 adalah", hasil)
hasil = b > 13
print(b, "> 13 adalah", hasil)

print(" ================== Lebih kecil dari 13 ")
hasil = a < 13
print(a, "< 13 adalah", hasil)
hasil = b < 13
print(b, "< 13 adalah", hasil)

print(" ================== Lebih besar atau sama dengan 13 ")
hasil = a >= 13
print(a, ">= 13 adalah", hasil)
hasil = b >= 13
print(b, ">= 13 adalah", hasil)

print(" ================== Lebih kecil atau sama dengan 13 ")
hasil = a <= 13
print(a, "<= 13 adalah", hasil)
hasil = b <= 13
print(b, "<= 13 adalah", hasil)

print(" ================== Sama dengan 13 ")
hasil = a == 13
print(a, "== 13 adalah", hasil)
hasil = b == 13
print(b, "== 13 adalah", hasil)
